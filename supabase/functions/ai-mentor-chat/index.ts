import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are an expert AI Interview Mentor for Indian students preparing for MNC (Multinational Company) job interviews.

Your specialties:
- Mock interview Q&A practice (HR, behavioral, situational)
- English communication coaching with Hindi-friendly explanations
- STAR method (Situation, Task, Action, Result) guidance  
- Resume tips and salary negotiation strategies
- Group Discussion (GD) techniques
- Body language and confidence building
- Industry-specific interview prep (IT, Data Science, Marketing, HR)
- Government exam interview preparation

Your tone: Warm, encouraging, like a senior mentor helping a younger student. Never condescending.

Guidelines:
- Give actionable, specific advice â€” not generic tips
- Use examples relevant to Indian corporate culture
- When correcting English, be gentle and show the improved version
- For interview questions, always provide a structured sample answer
- Use markdown formatting (bold, bullets, numbered lists) for clarity
- Keep responses focused and under 400 words unless a detailed explanation is needed
- Occasionally add motivational phrases in Hinglish when appropriate`;

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please wait a moment and try again." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please contact support." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      throw new Error("AI gateway error");
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("AI Mentor error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
